package br.com.gestaoeventos.bean;

/**
 *
 * @author cleiton
 */
public class HorarioEvento {
    
    private Integer idHorario;
    private String descricaoHorario;      

    /**
     * @return the idHorario
     */
    public Integer getIdHorario() {
        return idHorario;
    }

    /**
     * @param idHorario the idHorario to set
     */
    public void setIdHorario(Integer idHorario) {
        this.idHorario = idHorario;
    }

    /**
     * @return the descricaoHorario
     */
    public String getDescricaoHorario() {
        return descricaoHorario;
    }

    /**
     * @param descricaoHorario the descricaoHorario to set
     */
    public void setDescricaoHorario(String descricaoHorario) {
        this.descricaoHorario = descricaoHorario;
    }
    
    
    
}
